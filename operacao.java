package com.jackson.projeto;

public class operacao {
    public static void main(String[] args) {
        int n1 = 2;
        int n2 = 16;

        double soma = n1 + n2;
        double sub = n1 - n2;
        double multip = n1 * n2;
        double div = n1 / n2;

        System.out.println("A soma é: " + soma + "\n" + "A subtração é: "+ sub + "\n" + "A multiplicação é: "+ multip + "\n" + "A divisão é: "+ div);
    }
}
